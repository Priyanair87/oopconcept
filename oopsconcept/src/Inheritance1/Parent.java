package Inheritance1;

public class Parent {
	public void print()
	{
		System.out.println("hello this is public method");
	}
	protected void print1()
	{
		System.out.println("welcome to protected method ");
	}
	private void print2()
	{
		System.out.println(" oops concept of private method");
	}
	 void print3()
	{
		System.out.println("default method");
	}
}
